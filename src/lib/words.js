export const generateFriendCode = (firstName) => {
    const cleanName = (firstName || '').trim().split(' ')[0].toUpperCase().replace(/[^A-Z]/g, '');
    const safeName = cleanName || 'USER';
    const randomWord = FRIEND_WORDS[Math.floor(Math.random() * FRIEND_WORDS.length)].toUpperCase();
    
    // 50% chance to put word before or after
    return Math.random() > 0.5 ? `${safeName}-${randomWord}` : `${randomWord}-${safeName}`;
};
